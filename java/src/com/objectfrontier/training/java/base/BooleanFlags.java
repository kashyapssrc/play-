package com.objectfrontier.training.java.base;
public class BooleanFlags {

    public static void main(String args[]) {

        int aNumber = 17;
        Boolean flagOne = (aNumber > 20);
        Boolean flagTwo = (aNumber >= 17);
        Boolean flagThree = (aNumber < 10);
        Boolean flagFour = (aNumber == 18);
        System.out.println(flagOne);
        System.out.println(flagTwo);
        System.out.println(flagThree);
        System.out.println(flagFour);
    }
}

